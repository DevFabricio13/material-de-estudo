const numero = 21;

if (numero >= 0 && numero <= 5) {
    console.log('O numero é maior ou igual a 0')
} else if (numero >= 6 && numero <= 10) {
    console.log('O numero é maior ou igual a 6')
} else if (numero >= 11 && numero <= 20) {
    console.log('O numero é maior ou igual a 11')
} else {
    console.log(' O numero não está entre 0 e 20')
}