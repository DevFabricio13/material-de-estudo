// No JavaScript há duas maneiras de criar variáveis;
// Variáveis pode ser alterada durante a codificação;
// Variáveis como "let", varia, pode ser modificado;
let nome = "João";

console.log(nome, "nasceu em 2000"); 
console.log("Em 2021", nome, "conheceu Maria");
console.log(nome, "casou-se com Maria em 2024");
console.log("Maria teve", 1, "filho de", nome, "em 2026"); // Var + String + Number 
console.log("O nome do filho de João se chama 'Eduardo'"); // atenção sempre na pontuação de virgulas.

// Também posso criar uma variável e não colocar um valor ou iniciar ela. Ex:
let idade; // apenas declarei a variável.
idade = 20;
console.log(idade);
idade = 25;
console.log(idade);

let nomeCompletoDoCliente = "Fabricio Ribeiro"; // Essa variavel se chama "Case-Sensitive"
console.log(nomeCompletoDoCliente);            // Composta por letras minusculas e maiusculas.

let nomeAluno = "Richard";
let nomealuno = "Souza";
console.log(nomeAluno, nomealuno); // Essas variaveis são diferentes e possuem valores diferentes.

