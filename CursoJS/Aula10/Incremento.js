// Um operador de incremento é o "++"

let contador = 1;
contador++; // já é feito a incrementação do valor da variavel e atribuição também.
contador++; // 6
++contador; // 7
contador++; // 8
console.log(contador); // a variável está com o valor de "1", com a incrementação "++", o resultado será 2.

let contador1 = 2;
console.log(++contador1); // com a implementação em primeiro, já é feito a atribuição do valor.

let decremento = 10;
console.log(--decremento);

